    private void btnDonateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDonateActionPerformed
        // TODO add your handling code here:
        String message = txtmessage.getText();
        Date appDate = dateChooserField.getDate();
        String strDate = DateFormat.getDateInstance().format(appDate);
        
        DonorBankRequest dbr = new DonorBankRequest();
        dbr.setMessage(message);
        dbr.setAppointmentDate(strDate);
        dbr.setBloodType(cboxBlood1.getSelectedItem().toString());
        dbr.setReqType("donateBlood");
        dbr.setSender(userAccount);
        dbr.setStatus("Sent By Donor");
        
        Organization org = null;
        for (Organization organization : enterprise.getOrganizationDirectory().getOrganizationList()) {
            if (organization instanceof DonorBankOrganization) {
                org = organization;
                break;
            }
        }
        System.out.println("Organization :" + org);

        if (org != null) {
            int a = org.getWorkQueue().getWorkRequestList().hashCode();
            org.getWorkQueue().getWorkRequestList().add(dbr);
            userAccount.getWorkQueue().getWorkRequestList().add(dbr);
        }
        
        JOptionPane.showMessageDialog(null, "Blood Donation Requested Successfully");
        txtmessage.setText("");
        
    }//GEN-LAST:event_btnDonateActionPerformed

